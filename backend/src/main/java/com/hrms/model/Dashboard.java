package com.hrms.model;

public class Dashboard {

    private int totalEmployees;
    private int totalAttendance;
    private int totalLeaves;
    private int totalPayrolls;
    private int totalDepartments;
    private int totalRecruitments;
    private int totalPerformances;

    public Dashboard() {
    }

    public int getTotalEmployees() {
        return totalEmployees;
    }

    public void setTotalEmployees(int totalEmployees) {
        this.totalEmployees = totalEmployees;
    }

    public int getTotalAttendance() {
        return totalAttendance;
    }

    public void setTotalAttendance(int totalAttendance) {
        this.totalAttendance = totalAttendance;
    }

    public int getTotalLeaves() {
        return totalLeaves;
    }

    public void setTotalLeaves(int totalLeaves) {
        this.totalLeaves = totalLeaves;
    }

    public int getTotalPayrolls() {
        return totalPayrolls;
    }

    public void setTotalPayrolls(int totalPayrolls) {
        this.totalPayrolls = totalPayrolls;
    }

    public int getTotalDepartments() {
        return totalDepartments;
    }

    public void setTotalDepartments(int totalDepartments) {
        this.totalDepartments = totalDepartments;
    }

    public int getTotalRecruitments() {
        return totalRecruitments;
    }

    public void setTotalRecruitments(int totalRecruitments) {
        this.totalRecruitments = totalRecruitments;
    }

    public int getTotalPerformances() {
        return totalPerformances;
    }

    public void setTotalPerformances(int totalPerformances) {
        this.totalPerformances = totalPerformances;
    }
}